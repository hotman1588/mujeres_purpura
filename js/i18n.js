(function () {
  const STORAGE_KEY = "mp_lang";
  // Idiomas soportados y su presentación en el desplegable (bandera + código + nombre)
  const LANGS = {
    es: { flag: "🇪🇸", code: "ES", name: "Español" },
    en: { flag: "🇬🇧", code: "EN", name: "English" },
    fr: { flag: "🇫🇷", code: "FR", name: "Français" },
    it: { flag: "🇮🇹", code: "IT", name: "Italiano" }
  };
  const stored = localStorage.getItem(STORAGE_KEY);
  let currentLang = LANGS[stored] ? stored : "es";

  // Refleja el idioma activo en el botón del desplegable y marca la opción activa
  function updateLangUI(lang) {
    const info = LANGS[lang] || LANGS.es;
    const flagEl = document.querySelector("#langToggle .lang-current-flag");
    const codeEl = document.querySelector("#langToggle .lang-current-code");
    if (flagEl) flagEl.textContent = info.flag;
    if (codeEl) codeEl.textContent = info.code;
    document.querySelectorAll(".lang-option").forEach((opt) => {
      opt.classList.toggle("is-active", opt.dataset.lang === lang);
      opt.setAttribute("aria-selected", opt.dataset.lang === lang ? "true" : "false");
    });
  }

  function applyLanguage(lang) {
    const dict = translations[lang];
    if (!dict) return;
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.dataset.i18n;
      if (dict[key] === undefined) return;
      // Preservar el ícono del menú (.nav-ico) al traducir el texto
      const ico = el.querySelector(".nav-ico");
      el.textContent = dict[key];
      if (ico) el.insertBefore(ico, el.firstChild);
    });
    document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
      const key = el.dataset.i18nPh;
      if (dict[key] !== undefined) el.placeholder = dict[key];
    });
    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      const key = el.dataset.i18nAria;
      if (dict[key] !== undefined) el.setAttribute("aria-label", dict[key]);
    });
    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      const key = el.dataset.i18nTitle;
      if (dict[key] !== undefined) el.setAttribute("title", dict[key]);
    });
    document.documentElement.lang = lang;
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    updateLangUI(lang);
    window.dispatchEvent(new CustomEvent("mp:languagechange", { detail: { lang, dict } }));
  }

  window.MP_I18N = {
    t: (key) => (translations[currentLang] && translations[currentLang][key]) || key,
    lang: () => currentLang,
    apply: applyLanguage,
    applyCurrent: () => applyLanguage(currentLang)
  };

  function init() {
    const select = document.getElementById("langSelect");
    const btn = document.getElementById("langToggle");

    if (select && btn) {
      const menu = select.querySelector(".lang-menu");

      const openMenu = () => {
        select.classList.add("open");
        btn.setAttribute("aria-expanded", "true");
      };
      const closeMenu = () => {
        select.classList.remove("open");
        btn.setAttribute("aria-expanded", "false");
      };

      btn.addEventListener("click", (e) => {
        e.stopPropagation();
        select.classList.contains("open") ? closeMenu() : openMenu();
      });

      // Selección de idioma
      select.querySelectorAll(".lang-option").forEach((opt) => {
        const choose = () => {
          applyLanguage(opt.dataset.lang);
          closeMenu();
          btn.focus();
        };
        opt.addEventListener("click", choose);
        opt.addEventListener("keydown", (e) => {
          if (e.key === "Enter" || e.key === " ") { e.preventDefault(); choose(); }
        });
      });

      // Cerrar al hacer clic fuera o con Escape
      document.addEventListener("click", (e) => {
        if (!select.contains(e.target)) closeMenu();
      });
      document.addEventListener("keydown", (e) => {
        if (e.key === "Escape" && select.classList.contains("open")) { closeMenu(); btn.focus(); }
      });
    }

    applyLanguage(currentLang);
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
})();





